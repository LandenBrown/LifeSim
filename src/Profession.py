class Profession:
    def __init__(self, name, startingSalary, highSalary, lowSalary, position, positionMax, educationRequired):
        self.name = name
        self.startingSalary = startingSalary
        self.highSalary = highSalary
        self.lowSalary = lowSalary
        self.position = position
        self.positionMax = positionMax
        self.educationRequired = educationRequired